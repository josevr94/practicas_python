# el in es un elemento muy util para averiguar si tenenmod algun elemento dentro de otro 
# ejemplo: podemos preguntar si dentro de una lista esta sierto elemento con un in 
frutas= ["manzanas", "peras", "naranjas", "platanos"]
if "manzanas" in frutas:
    print("La fruta manzana está en la lista")

hola = "hola mundo como estan saludos desde costa rica"
if "costa" in hola:
    print("la palabra costa está en la cadena")