# uso de las F dentro del print
nombre = "jose"
edad = 29
print(f"hola {nombre} tu edad es {edad}")

# sumas dentro del print
precio1 = 1000
precio2 = 2000
print(f"El total es de {precio1 + precio2}")

# usando if con un f-string
temperatura = 31
print(f"la temperatura esta {"alta" if temperatura > 30 else "baja"}")